//add,sub,mul,div of two numbers

class maths
{
	public static void main(String args[])
	{
		int a = 10;
		int b = 5;
		int add,sub,mul,div;
		add = a + b;
		sub = a - b;
		mul = a * b;
		div = a / b;
		System.out.println("addition = "+add);
				
		System.out.println("subtraction = "+sub);

		System.out.println("multiplication = "+mul);

		System.out.println("division = "+div);

	}
}